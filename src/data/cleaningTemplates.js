// סיווג שורת חתימה לפי הקידומת (לקביעת סוג השדה ברכיב)
export function signoffKind(label) {
  if (label.startsWith('שדה חובה')) return 'name'
  if (label.startsWith('נבדק')) return 'check'
  return 'sign'
}

// בוני סקשנים מקוצרים
const quick = (id, title, items, shift) => ({
  id,
  kind: 'quick',
  title,
  items,
  ...(shift ? { shift } : {}),
})
const signed = (id, title, items, signoff, opts = {}) => ({
  id,
  kind: 'signed',
  title,
  groups: [{ items }],
  signoff,
  ...opts,
})
const signedG = (id, title, groups, signoff, opts = {}) => ({
  id,
  kind: 'signed',
  title,
  groups,
  signoff,
  ...opts,
})

